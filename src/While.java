public class While {
    public static void main(String[]args){
        //litri de benzina
        int litri_de_benzina = 5;
        while (litri_de_benzina > 0){
            System.out.println("Vrum Vrum");
            litri_de_benzina = litri_de_benzina-1;
        }
        System.out.println("Benzina pe terminate,alimenteaza!");
        System.out.println("STOP , masina nu mai are benzina");
    }
}
